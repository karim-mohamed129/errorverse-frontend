export default function ReplyModal({ open, onClose, parentId, onSubmit }: ReplyModalProps) {
  const [text, setText] = useState("");

  if (!open) return null;

  const handleSubmit = () => {
    onSubmit(text);
    setText("");
    onClose();
  };

  const modalStyle = {
    "--dashboard-card-image": `url(${cardBorderUri})`,
  } as React.CSSProperties;

  return (
    <div className="reply-modal-backdrop" role="presentation" onClick={onClose}>
      <div
        className="reply-modal"
        style={modalStyle}
        role="dialog"
        aria-modal="true"
        aria-labelledby="reply-modal-title"
        onClick={(event) => event.stopPropagation()}
      >
        <div className="reply-modal-top-glow" />

        <SectionHeader
          title="ADD YOUR REPLY"
          chipImage={chipShapeUri}
          showButton={false}
        />

        <div className="reply-modal-body">
          <label htmlFor="reply-modal-textarea" className="reply-modal-label">
            Write Your Comment
          </label>

          <div className="reply-modal-textarea-wrap">
            <textarea
              id="reply-modal-textarea"
              className="reply-modal-textarea"
              placeholder="Write Here........."
              value={text}
              onChange={(e) => setText(e.target.value)}
            />

            <ReplyModalIcons />
          </div>

          <div className="reply-modal-actions">
            <button
              type="button"
              className="reply-modal-cancel"
              onClick={onClose}
            >
              CANCEL
            </button>

            <button
              type="button"
              className="reply-modal-submit"
              onClick={handleSubmit}
            >
              SUBMIT
            </button>
          </div>
        </div>
      </div>
    </div>
  );
}
